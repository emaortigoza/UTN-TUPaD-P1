print(x == 8 or x < 10) # True
print(not (x == 8)) # False
print(not (x != 8)) # True
print(not (x > 8)) # True
print(not (x < 8)) # True
print(not (x >= 8)) # False
print(not (x <= 8)) # False
print(not (x == 8 and x < 10)) # False
